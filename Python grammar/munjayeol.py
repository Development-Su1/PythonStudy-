# 문자열 (46.58)
# 혼란을 줄이기 위해 가급적 주석은 작은 따옴표, 문자열은 큰 따옴표 사용을 권해드립니다.

sentence1 = '나는 소년입니다'
print(sentence1) # 나는 소년입니다.

sentence2 = "파이썬은 쉽다"
print(sentence2) # 파이썬은 쉽다

# 문자열은 한 줄이 아닌 여러 줄에 걸쳐서 작성할 수도 있습니다. 이 때는 작은 따옴표 또는 큰 따옴표 3개씩을 앞뒤로 감싸주면 됩니다.

sentence3 = """
나는 소년이고,
파이썬은 쉽다
"""
print(sentence3) # 나는 소년이고, 파이썬은 쉽다.